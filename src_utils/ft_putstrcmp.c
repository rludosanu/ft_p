#include "libutils.h"

char			*ft_putstrcmp(char *s1, char *s2)
{
	char		*t1;
	char		*t2;

	if (!s1 || !s2)
		return (NULL);
	t1 = (char *)s1;
	t2 = (char *)s2;
	while (*t1 != '\0' && *t2 != '\0' && *t1 == *t2)
	{
		t1++;
		t2++;
	}
	return (t2);
}
